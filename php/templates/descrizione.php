        i clienti possono consultare un catalogo di farmaci ed effettuare delle prenotazioni su di essi.<br>
        Un cliente può prenotare massimo 3 articoli per farmaco. Dopo la prenotazione il cliente puo consultare uno storico
        dei farmaci prenotati, dopo che il farmacista modifica lo stato della sua prenotazione in 'ritirato' un cliente puo lasciare un opinione su quel farmaco
        <br>
        Un cliente per poter lasciare un opinione su un farmaco deve avere almeno una prenotazione per quel farmaco in stato 'ritirato'
        <br>Un cliente può lasciare una sola opinione per farmaco
        <br><br>
        I farmacisti possono inserire dei nuovi farmaci nel catalogo e gestire lo stato delle prenotazioni dei clienti.
        Un farmacista può cercare la prenotazione di un cliente tramite il suo codice.
        <br>
        <br>
        I farmacisti ed i clienti essendo 2 soggetti separati possono accedere solamente alle specifiche sezioni del sito di cui hanno i permessi<br><br>
        In particolare un cliente può consultare il catalogo e le schede di ogni prodotto insieme al suo carrello e allo storico delle prenotazioni ma per esempio non puo ne visualizzare le pagine di controllo dei farmacisti ne eseguire le operazioni definite per essi (modifica prenotazioni ed inserimento nuovi farmaci)
        <br>
        <br>
        Ai farmacisti è possibile consultare il catalogo ma è vietata la prenotazione dei farmaci e l inserimento di un parere su un farmaco

        <br><br>Agli utenti non loggati non è permessa nessuna operazione

        <br><br>Nel caso si prova ad effettuare una di queste operazioni non avendone i permessi viene visualizzato il relativo messaggio di errore
