        WebPharma è la web app di una farmacia. Esistono 2 tipi di utenti: Farmacisti e clienti.
        i clienti possono consultare un catalogo di farmaci ed effettuare delle prenotazioni su di essi,
        Un cliente può prenotare massimo 3 articoli per farmaco. Dopo la prenotazione il cliente puo consultare uno storico
        dei farmaci prenotati, dopo che il farmacista modifica lo stato della sua prenotazione in 'ritirato' un cliente puo lasciare un opinione su quel farmaco
        I farmacisti possono inserire dei nuovi farmaci nel catalogo e gestire lo stato delle prenotazioni dei clienti.
